// 14. 變數名稱可視範圍介紹

#include <stdio.h>

int g = 1; // 此為全域變數，宣告在函式外面

int main(){
    printf("%d\n", g); // 在函式內仍可被呼叫
    int l = 2; // 此為區域變數，宣告在函式裡面
    printf("%d\n", l);
    {
        printf("%d\n", l); // 區域變數在更內部的函式仍可被呼叫
        int l = 3; // 此為區域變數，宣告在函式裡面
        printf("%d\n", l); // 被新定義的 l 覆蓋成 3
    }
    printf("%d\n", l); // 因為不受內部函式(3)的影響，仍為2
}